#!/bin/bash
################################################################################
# –°–∫—Ä–∏–ø—Ç –±–∞–Ω–Ω–µ—Ä–∞ SSH —Å –≥—Ä–∞–¥–∏–µ–Ω—Ç–Ω—ã–º –≤—ã–≤–æ–¥–æ–º
# –¶–µ–ª—å: –û—Ç–æ–±—Ä–∞–∂–µ–Ω–∏–µ –∫—Ä–∞—Å–∏–≤–æ–≥–æ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–æ–Ω–Ω–æ–≥–æ –±–∞–Ω–Ω–µ—Ä–∞ –ø—Ä–∏ SSH-–ø–æ–¥–∫–ª—é—á–µ–Ω–∏–∏
# –ê–≤—Ç–æ—Ä: Sandrick Tech
# –í–µ—Ä—Å–∏—è: 1.0
################################################################################

# –§—É–Ω–∫—Ü–∏—è: –í—ã–≤–æ–¥ —Å—Ç—Ä–æ–∫–∏ —Ç–µ–∫—Å—Ç–∞ —Å –ø–æ—Å–∏–º–≤–æ–ª—å–Ω—ã–º –≥—Ä–∞–¥–∏–µ–Ω—Ç–Ω—ã–º –ø–µ—Ä–µ—Ö–æ–¥–æ–º —Ü–≤–µ—Ç–∞
# –ü–∞—Ä–∞–º–µ—Ç—Ä—ã:
#   $1 - —Ç–µ–∫—Å—Ç –¥–ª—è –≤—ã–≤–æ–¥–∞
# –û–ø–∏—Å–∞–Ω–∏–µ: –°–æ–∑–¥–∞–µ—Ç –ø–ª–∞–≤–Ω—ã–π —Ü–≤–µ—Ç–æ–≤–æ–π –ø–µ—Ä–µ—Ö–æ–¥ –æ—Ç —Ç—ë–º–Ω–æ-—Å–∏–Ω–µ–≥–æ –∫ —Å–≤–µ—Ç–ª–æ–º—É
#           –∏—Å–ø–æ–ª—å–∑—É—è 256-—Ü–≤–µ—Ç–Ω—É—é –ø–∞–ª–∏—Ç—Ä—É —Ç–µ—Ä–º–∏–Ω–∞–ª–∞ (ANSI escape codes)
print_gradient_line() {
    local text="$1"              # –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤—Ö–æ–¥–Ω–æ–π —Ç–µ–∫—Å—Ç –≤ –ª–æ–∫–∞–ª—å–Ω—É—é –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é
    local start_color=45         # –ù–∞—á–∞–ª—å–Ω—ã–π —Ü–≤–µ—Ç –≥—Ä–∞–¥–∏–µ–Ω—Ç–∞ (—Ç—ë–º–Ω–æ-—Å–∏–Ω–∏–π, –∫–æ–¥ 45)
    local end_color=44           # –ö–æ–Ω–µ—á–Ω—ã–π —Ü–≤–µ—Ç –≥—Ä–∞–¥–∏–µ–Ω—Ç–∞ (—Å–≤–µ—Ç–ª–æ-—Å–∏–Ω–∏–π, –∫–æ–¥ 44)
    local length=${#text}        # –í—ã—á–∏—Å–ª—è–µ–º –¥–ª–∏–Ω—É —Ç–µ–∫—Å—Ç–æ–≤–æ–π —Å—Ç—Ä–æ–∫–∏
    local i=0                    # –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º —Å—á–µ—Ç—á–∏–∫ —Å–∏–º–≤–æ–ª–æ–≤

    # –¶–∏–∫–ª –ø–æ –∫–∞–∂–¥–æ–º—É —Å–∏–º–≤–æ–ª—É –≤ —Å—Ç—Ä–æ–∫–µ
    for (( i=0; i<length; i++ )); do
        local char="${text:$i:1}"  # –ò–∑–≤–ª–µ–∫–∞–µ–º –æ–¥–∏–Ω —Å–∏–º–≤–æ–ª –∏–∑ —Å—Ç—Ä–æ–∫–∏ –ø–æ –∏–Ω–¥–µ–∫—Å—É i
        # –í—ã—á–∏—Å–ª—è–µ–º –ø—Ä–æ–º–µ–∂—É—Ç–æ—á–Ω—ã–π —Ü–≤–µ—Ç –Ω–∞ –æ—Å–Ω–æ–≤–µ –ø–æ–∑–∏—Ü–∏–∏ —Å–∏–º–≤–æ–ª–∞
        # –§–æ—Ä–º—É–ª–∞: start + (end - start) * –ø–æ–∑–∏—Ü–∏—è / (–¥–ª–∏–Ω–∞-1)
        local color=$(( start_color + (end_color - start_color) * i / (length-1) ))
        # –í—ã–≤–æ–¥–∏–º —Å–∏–º–≤–æ–ª —Å –≤—ã—á–∏—Å–ª–µ–Ω–Ω—ã–º —Ü–≤–µ—Ç–æ–º —á–µ—Ä–µ–∑ ANSI escape –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å
        # \e[38;5;${color}m - —É—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç —Ü–≤–µ—Ç —Ç–µ–∫—Å—Ç–∞
        echo -ne "\e[38;5;${color}m${char}"
        # –°–±—Ä–∞—Å—ã–≤–∞–µ–º —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ –ø–æ—Å–ª–µ –∫–∞–∂–¥–æ–≥–æ —Å–∏–º–≤–æ–ª–∞
        echo -ne "\e[0m"
    done
    echo  # –ü–µ—Ä–µ—Ö–æ–¥ –Ω–∞ –Ω–æ–≤—É—é —Å—Ç—Ä–æ–∫—É –ø–æ—Å–ª–µ –≤—ã–≤–æ–¥–∞ –≤—Å–µ–π —Å—Ç—Ä–æ–∫–∏
}

# –§—É–Ω–∫—Ü–∏—è: –í—ã–≤–æ–¥ —Å—Ç—Ä–æ–∫–∏ —Å –≥—Ä–∞–¥–∏–µ–Ω—Ç–æ–º –æ—Ç –±–µ–ª–æ–≥–æ –∫ —Å–µ—Ä–æ–º—É
# –ò—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –¥–ª—è –≤—ã–≤–æ–¥–∞ —Ä–∞–º–æ–∫ –∏ –¥–µ–∫–æ—Ä–∞—Ç–∏–≤–Ω—ã—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤
print_gradient_line1() {
    local text="$1"              # –í—Ö–æ–¥–Ω–æ–π —Ç–µ–∫—Å—Ç
    local start_color=255        # –ë–µ–ª—ã–π —Ü–≤–µ—Ç (–º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ)
    local end_color=240          # –°–≤–µ—Ç–ª–æ-—Å–µ—Ä—ã–π —Ü–≤–µ—Ç
    local length=${#text}        # –î–ª–∏–Ω–∞ —Ç–µ–∫—Å—Ç–∞
    local i=0                    # –°—á–µ—Ç—á–∏–∫

    # –ê–Ω–∞–ª–æ–≥–∏—á–Ω—ã–π —Ü–∏–∫–ª –ø–æ—Å–∏–º–≤–æ–ª—å–Ω–æ–≥–æ –≤—ã–≤–æ–¥–∞ —Å –≥—Ä–∞–¥–∏–µ–Ω—Ç–æ–º
    for (( i=0; i<length; i++ )); do
        local char="${text:$i:1}"
        local color=$(( start_color + (end_color - start_color) * i / (length-1) ))
        echo -ne "\e[38;5;${color}m${char}"
        echo -ne "\e[0m"
    done
    echo
}

# –§—É–Ω–∫—Ü–∏—è: –í—ã–≤–æ–¥ —Å—Ç—Ä–æ–∫–∏ —Å –≥—Ä–∞–¥–∏–µ–Ω—Ç–æ–º –æ—Ç –∂–µ–ª—Ç–æ–≥–æ –∫ –±–µ–ª–æ–º—É
# –ò—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –¥–ª—è –∑–∞–≤–µ—Ä—à–∞—é—â–∏—Ö –¥–µ–∫–æ—Ä–∞—Ç–∏–≤–Ω—ã—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤
print_gradient_line2() {
    local text="$1"              # –í—Ö–æ–¥–Ω–æ–π —Ç–µ–∫—Å—Ç
    local start_color=230        # –ñ—ë–ª—Ç—ã–π —Ü–≤–µ—Ç
    local end_color=250          # –ü–æ—á—Ç–∏ –±–µ–ª—ã–π
    local length=${#text}        # –î–ª–∏–Ω–∞ —Å—Ç—Ä–æ–∫–∏
    local i=0                    # –°—á–µ—Ç—á–∏–∫ —Å–∏–º–≤–æ–ª–æ–≤

    # –¶–∏–∫–ª –ø–æ—Å–∏–º–≤–æ–ª—å–Ω–æ–≥–æ –≤—ã–≤–æ–¥–∞ —Å –≤—ã—á–∏—Å–ª–µ–Ω–∏–µ–º –ø—Ä–æ–º–µ–∂—É—Ç–æ—á–Ω—ã—Ö —Ü–≤–µ—Ç–æ–≤
    for (( i=0; i<length; i++ )); do
        local char="${text:$i:1}"
        local color=$(( start_color + (end_color - start_color) * i / (length-1) ))
        echo -ne "\e[38;5;${color}m${char}"
        echo -ne "\e[0m"
    done
    echo
}

################################################################################
# –û—Å–Ω–æ–≤–Ω–æ–π –±–ª–æ–∫ –≤—ã–≤–æ–¥–∞ –±–∞–Ω–Ω–µ—Ä–∞
################################################################################

# –ü—Ä–æ–≤–µ—Ä–∫–∞: –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è –ª–∏ —Å–∫—Ä–∏–ø—Ç –≤ SSH-—Å–µ—Å—Å–∏–∏
# –ü–µ—Ä–µ–º–µ–Ω–Ω–∞—è SSH_CONNECTION —É—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç—Å—è —Ç–æ–ª—å–∫–æ –ø—Ä–∏ SSH-–ø–æ–¥–∫–ª—é—á–µ–Ω–∏–∏
# if [ -n "$SSH_CONNECTION" ]; then

# –í—ã–≤–æ–¥ –≤–µ—Ä—Ö–Ω–µ–π –¥–µ–∫–æ—Ä–∞—Ç–∏–≤–Ω–æ–π —Ä–∞–º–∫–∏ ASCII-–∞—Ä—Ç –ª–æ–≥–æ—Ç–∏–ø–∞
print_gradient_line1 " ______ ______ ______ ______ ______ ______"
print_gradient_line1 "/_____//_____//_____//_____//_____//_____/"

# –í—ã–≤–æ–¥ ASCII-–∞—Ä—Ç –ª–æ–≥–æ—Ç–∏–ø–∞ "Sandrick Tech" —Å –≥—Ä–∞–¥–∏–µ–Ω—Ç–Ω—ã–º —ç—Ñ—Ñ–µ–∫—Ç–æ–º
# –ò—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è —à—Ä–∏—Ñ—Ç "Standard" –∏–∑ figlet
print_gradient_line "   _____                    __       _        __      ______             __  "
print_gradient_line "  / ___/ ____ _ ____   ____/ /_____ (_)_____ / /__   /_  __/___   _____ / /_ "
print_gradient_line "  \__ \ / __ \`// __ \ / __  // ___// // ___// //_/    / /  / _ \ / ___// __ \\"
print_gradient_line " ___/ // /_/ // / / // /_/ // /   / // /__ / ,<      / /  /  __// /__ / / / /"
print_gradient_line "/____/ \__,_//_/ /_/ \__,_//_/   /_/ \___//_/|_|    /_/   \___/ \___//_/ /_/ "

# –í—ã–≤–æ–¥ –Ω–∏–∂–Ω–µ–π –¥–µ–∫–æ—Ä–∞—Ç–∏–≤–Ω–æ–π —Ä–∞–º–∫–∏
print_gradient_line2 "                                   ______ ______ ______ ______ ______ ______"
print_gradient_line2 "                                  /_____//_____//_____//_____//_____//_____/"

################################################################################
# –ë–ª–æ–∫ —Å–±–æ—Ä–∞ —Å–∏—Å—Ç–µ–º–Ω–æ–π –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏
################################################################################

# –ó–∞–≥—Ä—É–∑–∫–∞ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏ –æ –¥–∏—Å—Ç—Ä–∏–±—É—Ç–∏–≤–µ –∏–∑ —Ñ–∞–π–ª–∞ lsb-release
# –û–ø—Ü–∏—è -r: –ø—Ä–æ–≤–µ—Ä—è–µ—Ç —Å—É—â–µ—Å—Ç–≤–æ–≤–∞–Ω–∏–µ –∏ —á–∏—Ç–∞–µ–º–æ—Å—Ç—å —Ñ–∞–π–ª–∞
[ -r /etc/lsb-release ] && . /etc/lsb-release

# –ï—Å–ª–∏ –æ–ø–∏—Å–∞–Ω–∏–µ –¥–∏—Å—Ç—Ä–∏–±—É—Ç–∏–≤–∞ –Ω–µ –ø–æ–ª—É—á–µ–Ω–æ, –∏—Å–ø–æ–ª—å–∑—É–µ–º —É—Ç–∏–ª–∏—Ç—É lsb_release
if [ -z "$DISTRIB_DESCRIPTION" ] && [ -x /usr/bin/lsb_release ]; then
    # -s: –∫–æ—Ä–æ—Ç–∫–∏–π —Ñ–æ—Ä–º–∞—Ç –≤—ã–≤–æ–¥–∞, -d: –æ–ø–∏—Å–∞–Ω–∏–µ –¥–∏—Å—Ç—Ä–∏–±—É—Ç–∏–≤–∞
    DISTRIB_DESCRIPTION=$(lsb_release -s -d)
fi

################################################################################
# –í—ã–≤–æ–¥ —Å–∏—Å—Ç–µ–º–Ω–æ–π –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏
################################################################################

# –†–∞–∑–¥–µ–ª–∏—Ç–µ–ª—å –ø–µ—Ä–µ–¥ —Å–∏—Å—Ç–µ–º–Ω–æ–π –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–µ–π
print_gradient_line1 "============================================="

# –í—ã–≤–æ–¥ —Ç–µ–∫—É—â–µ–π –¥–∞—Ç—ã –∏ –≤—Ä–µ–º–µ–Ω–∏ —Å —á–∞—Å–æ–≤—ã–º –ø–æ—è—Å–æ–º
# –§–æ—Ä–º–∞—Ç: –ì–ì–ì–ì-–ú–ú-–î–î –ß–ß:–ú–ú:–°–° TZ
echo -e "üìÖ  Date  : $(date '+%Y-%m-%d %H:%M:%S %Z')"

# –í—ã–≤–æ–¥ –≤–µ—Ä—Å–∏–∏ –æ–ø–µ—Ä–∞—Ü–∏–æ–Ω–Ω–æ–π —Å–∏—Å—Ç–µ–º—ã
# $DISTRIB_DESCRIPTION: –Ω–∞–∑–≤–∞–Ω–∏–µ –∏ –≤–µ—Ä—Å–∏—è –û–° –∏–∑ lsb-release
# $(uname -o): —Ç–∏–ø –û–° (–æ–±—ã—á–Ω–æ GNU/Linux)
echo -e "üìä  OS ver: $DISTRIB_DESCRIPTION $(uname -o) "

# –í—ã–≤–æ–¥ –∏–º–µ–Ω–∏ —Ö–æ—Å—Ç–∞ (–∏–º–µ–Ω–∏ –∫–æ–º–ø—å—é—Ç–µ—Ä–∞ –≤ —Å–µ—Ç–∏)
echo -e "üñ•   Hostname : $(hostname)"

# –í—ã–≤–æ–¥ –≤—Ä–µ–º–µ–Ω–∏ —Ä–∞–±–æ—Ç—ã —Å–∏—Å—Ç–µ–º—ã —Å –º–æ–º–µ–Ω—Ç–∞ –∑–∞–≥—Ä—É–∑–∫–∏
# uptime -p: —á–µ–ª–æ–≤–µ–∫–æ—á–∏—Ç–∞–µ–º—ã–π —Ñ–æ—Ä–º–∞—Ç (–Ω–∞–ø—Ä–∏–º–µ—Ä, "up 2 days, 5 hours")
echo -e "üïí  Uptime: $(uptime -p)"

# –í—ã–≤–æ–¥ IP-–∞–¥—Ä–µ—Å–∞ —Å–µ—Ä–≤–µ—Ä–∞
# hostname -I: –≤—ã–≤–æ–¥–∏—Ç –≤—Å–µ IP-–∞–¥—Ä–µ—Å–∞
# awk '{print $1}': –±–µ—Ä—ë–º —Ç–æ–ª—å–∫–æ –ø–µ—Ä–≤—ã–π –∞–¥—Ä–µ—Å (–æ–±—ã—á–Ω–æ –æ—Å–Ω–æ–≤–Ω–æ–π)
echo -e "üåê IP address: $(hostname -I | awk '{print $1}')"

# –û–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ –≤—Ä–µ–º–µ–Ω–∏ –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ –≤—Ö–æ–¥–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
# last: –ø–æ–∫–∞–∑—ã–≤–∞–µ—Ç –∏—Å—Ç–æ—Ä–∏—é –≤—Ö–æ–¥–æ–≤
# -n 2: –±–µ—Ä—ë–º –ø–æ—Å–ª–µ–¥–Ω–∏–µ 2 –∑–∞–ø–∏—Å–∏
# -i: –ø–æ–∫–∞–∑—ã–≤–∞–µ—Ç IP-–∞–¥—Ä–µ—Å–∞
# $USER: —Ç–µ–∫—É—â–∏–π –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å
# awk 'NR==2': –±–µ—Ä—ë–º –≤—Ç–æ—Ä—É—é —Å—Ç—Ä–æ–∫—É (–ø—Ä–µ–¥—ã–¥—É—â–∏–π –≤—Ö–æ–¥, –Ω–µ —Ç–µ–∫—É—â–∏–π)
LAST_LOGIN=$(last -n 2 -i $USER | awk 'NR==2 {print $3" "$4" "$5" "$6}')

# –ü—Ä–æ–≤–µ—Ä–∫–∞: –µ—Å–ª–∏ –∏—Å—Ç–æ—Ä–∏—è –≤—Ö–æ–¥–æ–≤ –ø—É—Å—Ç–∞, –∑–Ω–∞—á–∏—Ç —ç—Ç–æ –ø–µ—Ä–≤—ã–π –≤—Ö–æ–¥
if [ -z "$LAST_LOGIN" ]; then
    echo "üïí Last Login: first login"
else
    echo "üïí Last Login: $LAST_LOGIN"
fi

# –†–∞–∑–¥–µ–ª–∏—Ç–µ–ª—å –º–µ–∂–¥—É —Å–µ–∫—Ü–∏—è–º–∏ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏
print_gradient_line1 "---------------------------------------------"

# –í—ã–≤–æ–¥ –∑–∞–≥—Ä—É–∑–∫–∏ –ø—Ä–æ—Ü–µ—Å—Å–æ—Ä–∞ (Load Average)
# uptime: –≤—ã–≤–æ–¥–∏—Ç LA –∑–∞ 1, 5 –∏ 15 –º–∏–Ω—É—Ç
# awk -F'load average:': —Ä–∞–∑–¥–µ–ª—è–µ–º –≤—ã–≤–æ–¥ –ø–æ —Å—Ç—Ä–æ–∫–µ "load average:"
# {print $2}: –±–µ—Ä—ë–º –≤—Ç–æ—Ä—É—é —á–∞—Å—Ç—å (—Å–∞–º–∏ –∑–Ω–∞—á–µ–Ω–∏—è LA)
echo "üñ•   CPU load :   $(uptime | awk -F'load average:' '{ print $2 }')"

# –í—ã–≤–æ–¥ —Ç–æ–ø-3 –ø—Ä–æ—Ü–µ—Å—Å–æ–≤ –ø–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—é CPU
# ps -eo: –≤—ã–≤–µ—Å—Ç–∏ –∫–æ–ª–æ–Ω–∫–∏
# pid: ID –ø—Ä–æ—Ü–µ—Å—Å–∞, user: –≤–ª–∞–¥–µ–ª–µ—Ü, comm: –∏–º—è –∫–æ–º–∞–Ω–¥—ã, %cpu: % CPU, %mem: % –ø–∞–º—è—Ç–∏
# --sort=-%cpu: —Å–æ—Ä—Ç–∏—Ä–æ–≤–∫–∞ –ø–æ CPU –≤ –æ–±—Ä–∞—Ç–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ (—Å–∞–º—ã–µ –Ω–∞–≥—Ä—É–∂–µ–Ω–Ω—ã–µ –ø–µ—Ä–≤—ã–º–∏)
# head -n 4: –ø–µ—Ä–≤—ã–µ 4 —Å—Ç—Ä–æ–∫–∏ (–∑–∞–≥–æ–ª–æ–≤–æ–∫ + —Ç–æ–ø 3 –ø—Ä–æ—Ü–µ—Å—Å–∞)
ps -eo pid,user,comm,%cpu,%mem --sort=-%cpu | head -n 4

print_gradient_line1 "---------------------------------------------"

# –í—ã–≤–æ–¥ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è –æ–ø–µ—Ä–∞—Ç–∏–≤–Ω–æ–π –ø–∞–º—è—Ç–∏
# free -h: —á–µ–ª–æ–≤–µ–∫–æ—á–∏—Ç–∞–µ–º—ã–π —Ñ–æ—Ä–º–∞—Ç (KB, MB, GB)
# awk '/Mem:/': –±–µ—Ä—ë–º —Å—Ç—Ä–æ–∫—É —Å –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–µ–π –æ –ø–∞–º—è—Ç–∏
# {print $3 "/" $2}: –∑–∞–Ω—è—Ç–æ / –≤—Å–µ–≥–æ
echo "üíæ  Memory   :     $(free -h | awk '/Mem:/ {print $3 "/" $2}')"

# –í—ã–≤–æ–¥ —Ç–æ–ø-3 –ø—Ä–æ—Ü–µ—Å—Å–æ–≤ –ø–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—é –ø–∞–º—è—Ç–∏
# --sort=-%mem: —Å–æ—Ä—Ç–∏—Ä–æ–≤–∫–∞ –ø–æ –ø–∞–º—è—Ç–∏ –≤ –æ–±—Ä–∞—Ç–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ
ps -eo pid,user,comm,%cpu,%mem --sort=-%mem | head -n 4

print_gradient_line1 "--------------------------------------------"

